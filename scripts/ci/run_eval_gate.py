"""Run the retrieval-eval runner + check the result against a baseline.

CI gate for G4.3-T2 (#441). The workflow
``.github/workflows/eval-gate.yml`` runs this script on every PR
that touches ``backend/src/meho_backplane/retrieval/`` (the read
path the eval gates) or ``backend/src/meho_backplane/retrieval/eval/``
(the eval engine itself).

What the gate exercises today
-----------------------------

The v0.2 backplane has no live PG-backed retrieval substrate
available in the per-PR CI environment (Postgres + pgvector +
fastembed model + ingested kb seed would 5-10x the CI wall clock,
violating Goal #11 DoD bullet 4 — "PR CI <10 minutes"). This gate
therefore exercises the runner end-to-end against a **deterministic
stub retrieve_fn** that hits the kb seed corpus's first expected
slug per query — the "perfect-retrieval reference" case.

This catches:

* Regressions in the metric math (precision_at_k, MRR, coverage).
* Regressions in the verdict bands (green / yellow / red boundary).
* Regressions in the corpus loader (a malformed kb_queries.yaml
  fails the `load_corpus` call before the runner even starts).
* Regressions in the runner's per-surface dispatch (a broken
  hits-to-slugs mapping flips precision to 0).

What it does NOT catch (yet)
----------------------------

The live retrieval substrate (BM25 + cosine + RRF fusion in
``retriever.py``) is not exercised here. That coverage lives in
``tests/integration/test_retrieval_e2e.py`` and the
``chart.yml::helm-test`` job (Helm test Pod + pgvector). When the
Initiative #373 retire-checklist (T6 #445) wires a per-surface
threshold check against the live substrate, this gate becomes the
inner ring + a new "live eval" job becomes the outer ring.

Baseline + regression detection
-------------------------------

The script writes today's eval result to ``/tmp/eval-today.json``
and compares against the checked-in ``ci/eval-baseline.json``. A
regression > the per-metric epsilon (0.02) on any surface fails
the gate; a red verdict on any surface also fails. The baseline
is regenerated by an operator running:

    cd backend && uv run python ../scripts/ci/run_eval_gate.py --save-baseline ../ci/eval-baseline.json

and committing the result alongside the change that intentionally
moved the metrics.

Exit codes
----------

* 0 — green or yellow verdict, no regression vs baseline.
* 1 — red verdict, OR baseline regression detected, OR baseline
  file missing (fail-loud — see ``run_eval_gate``'s missing-baseline
  branch), OR runtime exception (corpus malformed, etc.).
"""

from __future__ import annotations

import argparse
import asyncio
import sys
import uuid
from collections.abc import Awaitable
from pathlib import Path

# Importable because the CI job runs `uv sync` in backend/ first +
# invokes this script with PYTHONPATH=backend/src.
from meho_backplane.retrieval.eval.baseline_io import (
    compare_baseline,
    load_baseline,
    save_baseline,
)
from meho_backplane.retrieval.eval.corpus import load_corpus
from meho_backplane.retrieval.eval.runner import eval_all
from meho_backplane.retrieval.retriever import RetrievalHit


def _build_perfect_retrieve_fn() -> Awaitable[list[RetrievalHit]]:
    """Stub retrieve_fn that returns the corpus's first expected hit per query.

    Same shape the unit tests use — see
    ``tests/test_retrieval_eval_runner.py::_make_perfect_retrieve_fn``.
    Models the "production retrieval is working perfectly" baseline;
    ensures the gate's reference run is always green so any drop is
    a regression in the runner / metrics / verdict layer.
    """
    corpus = load_corpus("kb")
    answer_map = {row.query: row.expected_hits[0] for row in corpus}

    async def perfect(
        *,
        tenant_id: uuid.UUID,
        query: str,
        source: str | None = None,
        kind: str | None = None,
        limit: int = 10,
    ) -> list[RetrievalHit]:
        slug = answer_map.get(query)
        if slug is None:
            return []
        return [
            RetrievalHit(
                document_id=uuid.uuid4(),
                tenant_id=tenant_id,
                source=source or "kb",
                source_id=slug,
                kind=f"{source or 'kb'}-entry",
                body=f"body for {slug}",
                doc_metadata={},
                fused_score=0.5,
                bm25_score=0.5,
                cosine_score=0.5,
                bm25_rank=1,
                cosine_rank=1,
            )
        ]

    return perfect  # type: ignore[return-value]


async def _run() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--save-baseline",
        type=Path,
        help="Write today's eval result to this path; do not compare.",
    )
    parser.add_argument(
        "--compare-baseline",
        type=Path,
        default=Path("ci/eval-baseline.json"),
        help="Compare today's eval against this baseline file.",
    )
    args = parser.parse_args()

    perfect = _build_perfect_retrieve_fn()
    today = await eval_all(
        tenant_id=uuid.UUID("00000000-0000-0000-0000-000000000001"),
        retrieve_fn=perfect,
    )

    if args.save_baseline:
        save_baseline(today, args.save_baseline)
        print(f"Wrote baseline to {args.save_baseline}")
        return 0

    # Print summary regardless of pass/fail so the workflow log
    # carries the numbers operators want to see at a glance.
    print(f"Eval verdict: {today.overall_verdict}")
    for s in today.surfaces:
        print(
            f"  {s.surface}: verdict={s.verdict} queries={s.query_count} "
            f"precision_at_5={s.precision_at_5:.3f} mrr={s.mrr:.3f} "
            f"coverage={s.coverage:.3f}"
        )

    if today.overall_verdict == "red":
        print("\nGATE FAILED: overall verdict is red", file=sys.stderr)
        return 1

    if not args.compare_baseline.exists():
        # Fail loud — a missing baseline file makes the gate a no-op,
        # which is the worst possible CI signal: green when there is
        # literally no comparison happening. Operators wouldn't notice
        # the regression check stopped running. The baseline is
        # checked-in (``ci/eval-baseline.json``) so a missing file
        # means it got renamed, deleted, or path-shadowed — every one
        # of those is a regression in the gate itself and must block
        # merge until corrected. To regenerate, run the script with
        # ``--save-baseline``.
        print(
            f"\nGATE FAILED: baseline missing at {args.compare_baseline}. "
            "Regenerate with `--save-baseline` and commit the result, "
            "or restore the file at the expected path.",
            file=sys.stderr,
        )
        return 1

    baseline = load_baseline(args.compare_baseline)
    regressions = compare_baseline(today, baseline)
    if regressions:
        print("\nGATE FAILED: regressions vs baseline:", file=sys.stderr)
        for r in regressions:
            print(f"  - {r}", file=sys.stderr)
        return 1

    print("\nGATE PASSED: no regression vs baseline")
    return 0


def main() -> int:
    return asyncio.run(_run())


if __name__ == "__main__":
    sys.exit(main())
